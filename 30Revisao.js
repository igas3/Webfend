let esta = 0



switch (esta) {
    case 0:
        console.log("Estação Verão");
        break;

    case 1:
        console.log("Estação Outono");
        break;

    case 2:
        console.log("Estação Inverno");
        break;

    case 3:
        console.log("Estação primavera");
        break;


    default:
        console.log("Os numeros só vão de 0 A 3");
        break;
}